defmodule AOC.Day02Test do
  use ExUnit.Case

  test "day01part1" do
    assert AOC.Day02.part1(
             "11-22,95-115,998-1012,1188511880-1188511890,222220-222224,1698522-1698528,446443-446449,38593856-38593862,565653-565659,824824821-824824827,2121212118-2121212124"
           ) == 1_227_775_554
  end

  test "day01part2" do
    assert AOC.Day02.part2("1111111-1111111") == 1_111_111
    assert AOC.Day02.part2("1212121212-1212121212") == 1_212_121_212

    assert AOC.Day02.part2(
             "11-22,95-115,998-1012,1188511880-1188511890,222220-222224,1698522-1698528,446443-446449,38593856-38593862,565653-565659,824824821-824824827,2121212118-2121212124"
           ) == 4_174_379_265
  end
end
